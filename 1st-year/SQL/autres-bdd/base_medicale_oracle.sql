drop table VISITETRAITEMENT;
drop table VISITES;
drop table TRAITEMENTS;
drop table PERSONNES;

create table PERSONNES (
 IdP VARCHAR2(5) 
       	   CONSTRAINT pkPERSONNES PRIMARY KEY,
           
      nom VARCHAR2(25),
      prenom VARCHAR2(25),      
      age NUMBER
      
      
);

create table VISITES (
      IdV VARCHAR2(5) 
       	   CONSTRAINT pkVISITES PRIMARY KEY,
      IdP VARCHAR2(5) REFERENCES PERSONNES,     
      date_visite NUMBER,
      tvh NUMBER,
      tvb NUMBER

);

create table TRAITEMENTS (
 IdT VARCHAR2(5) 
       	   CONSTRAINT pkTRAITEMENTS PRIMARY KEY,
           
      traitement VARCHAR2(30)
);

create table VISITETRAITEMENT (

  IdV VARCHAR2(5) REFERENCES VISITES,
    
  IdT VARCHAR2(5) REFERENCES TRAITEMENTS
 
);

